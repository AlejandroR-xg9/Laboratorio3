public abstract class Personal {
    int id;
    String nombre;
    String departamento;
    int experiencia;
    int salario;
    
    public Personal(int id, String nombre, String departamento, int experiencia, int salario) {
        this.id = id;
        this.nombre = nombre;
        this.departamento = departamento;
        this.experiencia = experiencia;
        this.salario = salario;
    }
}



